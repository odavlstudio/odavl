import * as React from "react"
import { cva, type VariantProps } from "class-variance-authority"
import { cn } from "@/lib/utils"

const odBadgeVariants = cva(
  "inline-flex items-center rounded-md border px-2.5 py-0.5 text-xs font-semibold transition-colors focus:outline-none focus:ring-2 focus:ring-ring focus:ring-offset-2",
  {
    variants: {
      variant: {
        default: "border-transparent bg-primary text-primary-foreground shadow hover:bg-primary/80",
        secondary: "border-transparent bg-secondary text-secondary-foreground hover:bg-secondary/80",
        success: "border-transparent bg-green-500 text-white hover:bg-green-500/80",
        warning: "border-transparent bg-yellow-500 text-white hover:bg-yellow-500/80",
        danger: "border-transparent bg-red-500 text-white hover:bg-red-500/80",
        outline: "text-foreground",
      },
    },
    defaultVariants: {
      variant: "default",
    },
  }
)

export interface ODBadgeProps
  extends React.HTMLAttributes<HTMLDivElement>,
    VariantProps<typeof odBadgeVariants> {}

function ODBadge({ className, variant, ...props }: ODBadgeProps) {
  return (
    <div className={cn(odBadgeVariants({ variant }), className)} {...props} />
  )
}

export { ODBadge, odBadgeVariants }